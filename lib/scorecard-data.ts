// ─── TEASER TEXT ENGINE ───────────────────────────────────────────────────────
// Answer-driven. 8+ variants. Specific enough to feel accurate.

export function getTeaserText(scores: Scores): string {
  const d = computeDimensions(scores);

  const ownerSelling  = scores["owner-selling"]        ?? 0;
  const repRetention  = scores["rep-retention"]        ?? 0;
  const closeTracking = scores["close-tracking"]       ?? 0;
  const onboarding    = scores["onboarding"]           ?? 0;
  const ownerDep      = scores["owner-dependency"]     ?? 0;
  const playbook      = scores["documented-playbook"]  ?? 0;
  const revPred       = scores["revenue-predictability"] ?? 0;
  const rideAlongs    = scores["ride-alongs"]          ?? 0;
  const followup      = scores["pipeline-followup"]    ?? 0;

  if (d.overall < 30) {
    return "Your scores across all 5 dimensions are in the early-stage range — which means the opportunity here is enormous. Your full report identifies the 3 highest-leverage starting points based specifically on your answers, ranked by ROI.";
  }

  if (ownerSelling <= 2 && d.process < 55) {
    return `Your owner-selling and process scores are flagging the pattern we see most often in high-performing contractor owners: you're the ceiling. The business performs because you perform — and your full report quantifies exactly what that's costing you in time, revenue, and eventual exit value.`;
  }

  if (repRetention <= 2 && onboarding <= 2) {
    return `Your people scores reveal a revolving-door pattern — good people coming in, not staying long enough to peak. The data on this is clear: 70% of rep failures are onboarding failures, not talent failures. Your full report shows exactly where the structure breaks down.`;
  }

  if (closeTracking <= 2 && followup <= 2) {
    return `Two of your scores are flagging a blind spot: you're running the business without visibility into close rate or follow-up discipline. Your full report shows what that invisible gap is likely costing you in monthly revenue — the number usually surprises people.`;
  }

  if (ownerDep <= 2 && playbook <= 2 && d.overall >= 50) {
    return `Your operational scores are solid — process, team, and coaching are working. The gap your full report identifies is in valuation readiness: the business performs well, but it performs because of YOU, not because of a documented system. That distinction is worth real money at exit.`;
  }

  if (revPred <= 2) {
    return `Your revenue predictability score is flagging volatility that sophisticated buyers price as risk. Even if annual revenue looks strong, month-to-month swings reduce EBITDA multiples by 1–2× in most specialty contractor transactions. Your full report shows the fix.`;
  }

  if (rideAlongs <= 2 && d.coaching < 40) {
    return `Your coaching scores suggest your reps are practicing on live prospects instead of in structured environments. That's where deals are being lost invisibly — not from lack of effort, but from lack of deliberate skill-building. Your full report identifies the highest-leverage intervention.`;
  }

  if (d.overall >= 65) {
    return `Your overall score puts you in the top 20% of contractors who've taken this assessment. Your full report identifies the specific 2–3 gaps between where you are and peak performance — at this stage, it's optimization, not rebuilding.`;
  }

  return `Your scorecard reveals a specific pattern across 5 dimensions — with a clear primary gap between your current trajectory and where the highest-performing contractors in your space are operating. Your full report shows exactly where that gap is and what each piece is worth to fix.`;
}

