// 3. Attrition-Plagued   → rep-retention ≤ 2 AND people < 45 (revolving door)
// 4. Growth-Stalled      → overall 35–60, no dominant weakness (hitting ceiling)
// 5. Exit-Minded         → valuation < 40 AND overall ≥ 50 (decent ops, exit gap specifically)
// 6. Scale-Ready         → overall ≥ 65 (optimizing, not rebuilding)
//
// Exit-Minded ONLY fires when overall is reasonably strong (≥50)
