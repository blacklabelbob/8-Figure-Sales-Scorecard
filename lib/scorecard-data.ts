export function formatPhone(value: string): string {
  const digits = value.replace(/\D/g, "").substring(0, 10);
  if (digits.length === 0) return "";
  if (digits.length <= 3) return `(${digits}`;
  if (digits.length <= 6) return `(${digits.slice(0, 3)}) ${digits.slice(3)}`;
  return `(${digits.slice(0, 3)}) ${digits.slice(3, 6)}-${digits.slice(6)}`;
}

export function isValidPhone(phone: string): boolean {
  const digits = phone.replace(/\D/g, "");
  if (digits.length !== 10) return false;
  if (digits[0] === "0" || digits[0] === "1") return false;
  if (/^(\d)\1{9}$/.test(digits)) return false;
  const fakes = ["1234567890","0987654321","1231231234","1111111111",
    "2222222222","3333333333","4444444444","5555555555",
    "6666666666","7777777777","8888888888","9999999999","0000000000"];
  return !fakes.includes(digits);
}

export function isValidEmail(email: string): boolean {
  if (!/^[^\s@]+@[^\s@]+\.[^\s@]{2,}$/.test(email)) return false;
  const fakeDomains = ["test.com","example.com","fake.com","temp.com",
    "noreply.com","mailinator.com","guerrillamail.com","throwaway.com","yopmail.com"];
  const domain = email.split("@")[1]?.toLowerCase();
  return !(domain && fakeDomains.includes(domain));
}
